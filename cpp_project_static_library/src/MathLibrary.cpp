
#include "MathLibrary.h"  // import Arithmetic Class 

namespace MathLibrary
{  

	double Arithmetic::Add(double x, double y)
	{
		return x + y;
	}	

	double Arithmetic::Subtract(double x, double y)
	{
		return x - y;
	}
		
	double Arithmetic::Multiply(double x, double y)
	{
		return x * y;
	}
	
	double Arithmetic::Divide(double x, double y)
	{
		return x / y;
	}
	
}	
	